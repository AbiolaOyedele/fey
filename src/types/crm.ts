export type ContactStatus  = 'active' | 'idle' | 'completed'
export type SenderType     = 'owner' | 'client'
export type ContractStatus = 'draft' | 'sent' | 'signed' | 'declined'
export type FormStatus     = 'draft' | 'sent' | 'submitted'
export type UploaderType   = 'owner' | 'client'

export interface CrmContact {
  id: string
  owner_id: string
  name: string
  email: string | null
  phone: string | null
  company: string | null
  avatar_url: string | null
  status: ContactStatus
  portal_enabled: boolean
  portal_welcome_message: string | null
  /** Short 8-char alphanumeric invite code. Used in join links. */
  invite_code: string | null
  created_at: string
  updated_at: string
}

export interface PortalUser {
  /** A plain UUID — NOT linked to auth.users. Generated by gen_random_uuid(). */
  id:             string
  contact_id:     string
  owner_id:       string
  /** The workspace this login belongs to (fey_settings.workspace_slug) */
  workspace_slug: string
  name:           string
  email:          string
  avatar_url:     string | null
  created_at:     string
}

