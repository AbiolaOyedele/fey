export type FormFieldType =
  | 'title'       // section heading — no input
  | 'text'        // short answer
  | 'textarea'    // long answer
  | 'multiselect' // checkboxes, multiple options
  | 'select'      // single select / radio
  | 'file'        // file upload
  | 'date'        // date picker
  | 'email'       // email input
  | 'phone'       // phone input

export interface FormField {
  id: string
  type: FormFieldType
  label: string
  placeholder: string | null
  required: boolean
  options: string[]
}

